    private void runTimerTaskUpdateCases() {
        
        TimerTask task = new TimerTask(){
	
            @Override
            public void run(){
                System.out.println("[Timer] Mise à jour information");
                updateCases();
            }	
	};
		
	Timer timer = new Timer();
	timer.scheduleAtFixedRate(task, 0, 1000*60*60);
    }
    
    public static void notificationChangementEtat(String idFiducial){
        IdToCode id = new IdToCode();
        String code = id.getCodeCase(idFiducial);
        cases.get(code).setMedicamentPri(true);
        updateCases();
    }
    
