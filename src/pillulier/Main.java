    private static void updateCases() {
        aPrendre = false;
        retards = 0;
        erreurs = 0;
        
        Iterator it = cases.entrySet().iterator();
        while (it.hasNext()) {
            Map.Entry pairs = (Map.Entry)it.next();
            Case c = (Case) pairs.getValue();
            int res = c.updateCouleur();
            if(res == 1){
                aPrendre = true;
            }
            else if(res == -1){
                retards++;
            }
            else if(res == -2){
                erreurs++;
            }
        }
        
        updateAffichageInfo();
    }

    private static void updateAffichageInfo() {
        if(aPrendre){
            jLabelAPrendre.setText("1");
        }
        else{
            jLabelAPrendre.setText("0");
        }
        
        jLabelMedicamentsRetard.setText(String.valueOf(retards));
        jLabelMedicamentsErreur.setText(String.valueOf(erreurs));

    }

