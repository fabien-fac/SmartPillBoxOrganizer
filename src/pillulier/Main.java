    JPanel getTitreLigne(String titre){
        JPanel panel = new JPanel(new BorderLayout());
        
        JLabel label = new JLabel(titre);
        label.setFont (label.getFont ().deriveFont (18.0f));       
        panel.add(label, BorderLayout.CENTER);
        panel.setBackground(Color.WHITE);
        return panel;
    }
    
    JLabel getTitreLabel(String titre){
        JLabel label = new JLabel(titre, SwingConstants.CENTER);
        label.setFont (label.getFont ().deriveFont (18.0f));
        return label;
    }
    
    JPanel getJPanel(String code){
        JPanel panel = new JPanel(new BorderLayout());
        panel.setBackground(Color.decode("#c8c8c8"));
        
        JLabel labelLed = new JLabel("•");
        labelLed.setForeground(Color.WHITE);
        labelLed.setFont (labelLed.getFont ().deriveFont (100.0f));
        labelLed.setHorizontalAlignment(JLabel.CENTER);
        cases.put(code, new Case(code, labelLed));
        panel.add(labelLed, BorderLayout.CENTER);
        panel.setBorder(BorderFactory.createLineBorder(Color.BLACK));
        
        panel.addMouseListener(getMouseListener(code, panel));
        return panel;
    }

